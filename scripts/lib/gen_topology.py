# ── Static Ansible inventory ──────────────────────────────────────────────────
# Used in dev before Foreman is fully populated.
# Foreman dynamic inventory takes over once hosts are registered.

def gen_inventory(topology, env, location, domain, cfg):
    bubbles = {k: v for k, v in topology["bubbles"].items() if not k.startswith("_")}
    lines = [
        "# Auto-generated from config/topology.json — do not edit manually.",
        "# This static inventory is used during initial deployment.",
        "# After registration, ansible/inventory/foreman.yml is the live source.",
        "",
        "[all:vars]",
        f"ansible_user=admin",
        f"ansible_ssh_common_args='-o StrictHostKeyChecking=no'",
        "",
    ]

    for bname, bubble in bubbles.items():
        lines += [f"[bubble_{bname}]"]
        for m in bubble["members"]:
            container = f"{env}-{location}-srv-{bname}-{m['name']}"
            lines.append(f"{container} ansible_host={m['ip']}")
        lines.append("")

    # All bubble members in one group for baseline playbook
    lines += ["[bubble_all:children]"]
    for bname in bubbles:
        lines.append(f"bubble_{bname}")
    lines.append("")

    # Function groups (function_web, function_db, etc.) across all bubbles
    func_groups: dict = {}
    for bname, bubble in bubbles.items():
        for m in bubble["members"]:
            func = m["function"]
            container = f"{env}-{location}-srv-{bname}-{m['name']}"
            func_groups.setdefault(func, []).append(container)

    for func, hosts in func_groups.items():
        lines += [f"[function_{func}]"]
        lines += hosts
        lines.append("")

    return "\n".join(lines)


# ── Bubble summary (printed to stdout for startup.sh) ─────────────────────────

def gen_bubble_list(topology, env, location, domain):
    """Return list of (bubble_name, [(container_name, ip, role), ...])"""
    bubbles = {k: v for k, v in topology["bubbles"].items() if not k.startswith("_")}
    result = []
    for bname, bubble in bubbles.items():
        members = []
        for m in bubble["members"]:
            container = f"{env}-{location}-srv-{bname}-{m['name']}"
            members.append((container, m["ip"], m.get("role", "")))
        result.append((bname, members))
    return result


def main():
    if len(sys.argv) < 7:
        print("Usage: gen_topology.py <topology.json> <light_root> <env> <location> <domain> <light.env>")
        sys.exit(1)

    topology_path, light_root, env, location, domain, env_path = sys.argv[1:7]
    root = Path(light_root)

    with open(topology_path) as f:
        topology = json.load(f)

    cfg = load_env(env_path) if Path(env_path).exists() else {}

    # Docker Compose overlay
    compose_out = root / "environments/dev/docker/docker-compose.topology.yml"
    compose_out.write_text(gen_compose(topology, env, location, domain, cfg))
    print(f"  wrote {compose_out}")

    # DNS zone additions (included by db.simple-test.org via $INCLUDE)
    dns_out = root / "environments/dev/docker/config/dns/zones/db.topology"
    dns_out.write_text(gen_dns(topology, env, location, domain))
    print(f"  wrote {dns_out}")

    # Ansible group_vars per bubble
    gvars_dir = root / "ansible/group_vars"
    gvars_dir.mkdir(exist_ok=True)
    bubbles = {k: v for k, v in topology["bubbles"].items() if not k.startswith("_")}
    for bname, bubble in bubbles.items():
        out = gvars_dir / f"bubble_{bname}.yml"
        out.write_text(gen_group_vars(bname, bubble, env, location, domain, cfg))
        print(f"  wrote {out}")

    # Static inventory (used during initial deploy, before Foreman is populated)
    inv_out = root / "ansible/inventory/topology.ini"
    inv_out.write_text(gen_inventory(topology, env, location, domain, cfg))
    print(f"  wrote {inv_out}")

    # Vagrantfile for hybrid mode (Docker mgmt zone + Vagrant workload VMs)
    vagrant_dir = root / "environments/dev/vagrant"
    vagrant_dir.mkdir(exist_ok=True)
    vagrant_out = vagrant_dir / "Vagrantfile.topology"
    vagrant_out.write_text(gen_vagrantfile(topology, env, location, domain, cfg))
    print(f"  wrote {vagrant_out}")

    # Machine-readable bubble list for startup.sh
    for bname, members in gen_bubble_list(topology, env, location, domain):
        for container, ip, role in members:
            print(f"  MEMBER:{bname}:{container}:{ip}:{role}")


if __name__ == "__main__":
    main()
